//For nos ayuda a realizar una iteracion con cada uno de los elementos en una lista
// La estructura de un for es la siguiente:
// for(variable, condicion, incremento) {Codigo a ejecutar}

let list = ["eat", "sleep", "code", "repeat"];

for (let i = 0; i < list.length; i++) {
  console.log(i);
  console.log(list[i]);
}
